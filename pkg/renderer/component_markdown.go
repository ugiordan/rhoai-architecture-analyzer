// renderOperatorConfigMarkdownSection renders operator config split by category.
func renderOperatorConfigMarkdownSection(b *strings.Builder, data map[string]interface{}) {
	items := getSlice(data, "operator_config")
	if len(items) == 0 {
		return
	}
	b.WriteString("\n## Operator Config\n\n")
	images := make([]map[string]interface{}, 0)
	envVars := make([]map[string]interface{}, 0)
	other := make([]map[string]interface{}, 0)
	for _, oc := range items {
		switch getStr(oc, "category", "") {
		case "image":
			images = append(images, oc)
		case "env_var":
			envVars = append(envVars, oc)
		default:
			other = append(other, oc)
		}
	}
	renderConfigSubTable := func(title string, entries []map[string]interface{}) {
		if len(entries) == 0 {
			return
		}
		b.WriteString(fmt.Sprintf("### %s\n\n", title))
		b.WriteString("| Name | Value | Source |\n")
		b.WriteString("|------|-------|--------|\n")
		for _, e := range entries {
			b.WriteString(fmt.Sprintf("| %s | %s | %s |\n",
				escapeMdCell(getStr(e, "name", "")),
				escapeMdCell(getStr(e, "value", "")),
				sourceLink(data, getStr(e, "source", "")),
			))
		}
		b.WriteString("\n")
	}
	renderConfigSubTable("Image References", images)
	renderConfigSubTable("Environment Variables", envVars)
	renderConfigSubTable("Other Constants", other)
}

