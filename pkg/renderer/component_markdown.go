// formatPorts formats port entries for display.
func formatPorts(ports []map[string]interface{}) string {
	parts := make([]string, 0, len(ports))
	for _, p := range ports {
		port := getInt(p, "port")
		proto := getStr(p, "protocol", "TCP")
		name := getStr(p, "name", "")
		if name != "" {
			parts = append(parts, fmt.Sprintf("%d/%s (%s)", port, proto, name))
		} else {
			parts = append(parts, fmt.Sprintf("%d/%s", port, proto))
		}
	}
	return strings.Join(parts, ", ")
}
